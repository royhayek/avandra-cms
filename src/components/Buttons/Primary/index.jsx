// ------------------------------------------------------------ //
// ------------------------- Packages ------------------------- //
// ------------------------------------------------------------ //
import React from "react";
// ------------------------------------------------------------ //
// ------------------------ Components ------------------------ //
// ------------------------------------------------------------ //
import { Button as MUIButton } from "@mui/material";
// ------------------------------------------------------------ //
// ------------------------- Utilities ------------------------ //
// ------------------------------------------------------------ //
import useStyles from "./styles.js";
import { useIsSmall } from "helpers/index.js";

// ------------------------------------------------------------ //
// ------------------------- Component ------------------------ //
// ------------------------------------------------------------ //

const Button = ({
  variant = "contained",
  text = "Submit",
  onClick,
  ...props
}) => {
// --------------------------------------------------------- //
// ------------------------ Static ------------------------- //
  const classes = useStyles();
  const isSmall = useIsSmall();
// ----------------------- /Static ------------------------- //
// --------------------------------------------------------- //

  return (
    <MUIButton
      disableElevation
      variant={variant}
      onClick={onClick}
      className={classes.button}
      size={isSmall ? "small" : "large"}
      {...props}
    >
      {text}
    </MUIButton>
  );
};

export default Button;
